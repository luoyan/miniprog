package com.luoyan.syntax;

public class FreeMarkerObject {
	  private String name;
	  private String developer;

	  public FreeMarkerObject(String name, String developer) {
	    this.name = name;
	    this.developer = developer;
	  }

	  public String getName() {
	    return name;
	  }

	  public String getDeveloper() {
	    return developer;
	  }
}
